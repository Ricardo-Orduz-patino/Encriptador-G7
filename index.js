// Función para encriptar el texto ingresado por el usuario
function encriptar() {
  // Obtener el texto del elemento HTML por su ID
  let texto = document.getElementById("texto").value;
  // Referencias a los elementos del DOM para mostrar mensajes y cambiar imágenes
  let tituloMensaje = document.getElementById("titulo-mensaje");
  let parrafo = document.getElementById("parrafo");
  let muñeco = document.getElementById("muñeco");

  // Reemplazo de vocales específicas por códigos predefinidos para encriptar
  let textoCifrado = texto
    .replace(/e/gi, "enter")
    .replace(/i/gi, "imes")
    .replace(/a/gi, "ai")
    .replace(/o/gi, "ober")
    .replace(/u/gi, "ufat");

  // Verificar si se ingresó texto para encriptar
  if (texto.length != 0) {
    document.getElementById("texto").value = textoCifrado;
    tituloMensaje.textContent = "Texto encriptado con éxito";
    parrafo.textContent = "";
    muñeco.src = "./img/encriptado.jpg"; // Cambiar la imagen a una que indique éxito
  } else {
    muñeco.src = "./img/muñeco.png";
    tituloMensaje.textContent = "Ningún mensaje fue encontrado";
    parrafo.textContent = "Ingresa el texto que deseas encriptar o desencriptar";
    swal("Ooops!", "Debes ingresar un texto", "warning"); // Alerta para indicar que no se ingresó texto
  }
}

